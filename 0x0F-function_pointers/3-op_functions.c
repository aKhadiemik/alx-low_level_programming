#include "3-calc.h"

/**
 * op_add - outputs sum of integers a & b
 *
 * @a: integer
 * @b: integer
 *
 * Return: sum
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - outputs difference of integers a & b
 *
 * @a: integer
 * @b: integer
 *
 * Return: difference
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - outputs product of integers a & b
 *
 * @a: int
 * @b: int
 *
 * Return: product
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - outputs division of ints a & b
 *
 * @a: int
 * @b: int
 *
 * Return: result of division
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - outputs modulus of ints a & b
 *
 * @a: int
 * @b: int
 *
 * Return: remainder
 */

int op_mod(int a, int b)
{
	return (a % b);
}
